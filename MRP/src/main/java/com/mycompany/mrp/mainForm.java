    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int indiceSeleccionado = jComboBox1.getSelectedIndex();
        System.out.println("Seleccionado: " + indiceSeleccionado);
        if (indiceSeleccionado == 0) {
            Integer inv = Integer.parseInt(TFinv.getText());
            Integer q = Integer.parseInt(TFq.getText());
            String mpr = TFmrp.getText();
            Integer stock = Integer.parseInt(TFstock.getText());
            Integer nivel = Integer.parseInt(TFnivel.getText());
            Integer ts = Integer.parseInt(TFts.getText());
            Integer multi = Integer.parseInt(TFmulti.getText());
            String req = TFrequerimietos.getText();
            n = new Nodo(req, nivel, mpr, inv, ts, stock, multi, q);
            this.arbol.add(n);
            jComboBox1.addItem(mpr + ":" + nivel);

        } else {
            String seleccionado = jComboBox1.getSelectedItem().toString();
            if (this.buscar(seleccionado)) {
                System.out.println("Dentro");
                Integer inv = Integer.parseInt(TFinv.getText());
                Integer q = Integer.parseInt(TFq.getText());
                String mpr = TFmrp.getText();
                Integer stock = Integer.parseInt(TFstock.getText());
                Integer nivel = Integer.parseInt(TFnivel.getText());
                Integer ts = Integer.parseInt(TFts.getText());
                Integer multi = Integer.parseInt(TFmulti.getText());
                String req = TFrequerimietos.getText();
                n = new Nodo(req, nivel, mpr, inv, ts, stock, multi, q, this.padre);
                this.arbol.add(n);
                jComboBox1.addItem(mpr + ":" + nivel);
                this.padre = null;
            }
        }
        //Limpiando cajas de texto
        TFrequerimietos.setText("");
        TFmrp.setText("");
        TFq.setText("");
        TFinv.setText("");
        TFstock.setText("");
        TFnivel.setText("");
        TFts.setText("");
        TFmulti.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

