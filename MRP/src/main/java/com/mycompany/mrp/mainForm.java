    public boolean buscar(String cd) {
        String[] partes = cd.split(":");
        String letra = partes[0];
        Integer numero = Integer.parseInt(partes[1]);
        for (int i = 0; i < this.arbol.size(); i++) {
            Nodo nodoABuscar = this.arbol.get(i);
            if (nodoABuscar.getMrp().equals(letra) && nodoABuscar.getNivel().equals(numero)) {
                this.padre = nodoABuscar;
                System.out.println("Si estaba");
                return true;
            }
        }
        return false;
    }

